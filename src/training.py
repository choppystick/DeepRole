"""
Training pipeline.

Implements Algorithm 3 (ENDTOENDTRAIN / GENERATEDATAPOINTS) of Serrino et al.
(2019). Trains the 45-network ValueBank in dependency-graph order: each
(proposer, s, f) network is trained on data generated by SOLVESITUATION
(CFRSolver), which uses the value bank's already-trained downstream networks
at depth-limited leaves.

For laptop scale (Q4=a) we expose configurable knobs and ship laptop-friendly
defaults. The full paper scale (120K samples × 1500 iters per network) is
not the default but is reachable by setting the knobs.

Public API:
  generate_datapoints(s, f, value_bank, n_samples, n_cfr_iters, ...)
    → list of (proposer:int, belief:np.ndarray(60,), V_target:np.ndarray(60,))
  train_value_network(net, dataset, *, epochs, batch_size, lr, val_split)
    → dict with 'train_losses' and 'val_losses' lists per epoch
  endtoend_train(value_bank, *, n_samples_per_part, n_cfr_iters, epochs, ...)
    → trained ValueBank (in place; returns same instance for convenience)

Game-part dependency graph (paper Figure 1, left):
  (s, f) depends on (s+1, f) and (s, f+1) — i.e. on game-parts deeper in the
  game. We train deepest first (s=2, f=2), so by the time CFR reaches a
  ValueNetLeaf inside the subgame we're sampling for, that leaf's network is
  already trained.

Notes on V target:
  CFRSolver returns counterfactual values V[i, j] of shape (5, 15). The value
  network's output is per-assignment "P(good wins)" w[a] of shape (60,). To
  derive a target w from CFR's V, we invert the readout: the network's output
  must approximately reproduce w·M_i·b — the simplest target is the
  "probability-weighted value at the LS info set" averaged appropriately.
  See _v_target_from_cfr below.
"""
